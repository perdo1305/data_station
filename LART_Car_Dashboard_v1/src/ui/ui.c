// Screen change names for logging (1-based EEZ-Flow IDs)
static const char *ui_screen_name(int eez_id) {
    static const char *names[] = { "?", "Driver View", "Autonomous", "Debug Autonomous", "Debug" };
    if (eez_id >= 1 && eez_id <= 4) return names[eez_id];
    return "?";
}

// Pending screen change (1-based EEZ-Flow ID, 0 = no pending change).
// Set by ui_set_screen(), consumed at the top of the next ui_tick()
// BEFORE eez_flow_tick() runs, preventing state corruption and crashes.
static int g_pending_screen_id = 0;

void ui_tick() {
    // Apply any pending screen change FIRST, before eez_flow_tick().
    if (g_pending_screen_id != 0) {
        int16_t current = eez_flow_get_current_screen(); // 1-based
        int target = g_pending_screen_id;
        g_pending_screen_id = 0;

        if (target >= _SCREEN_ID_FIRST && target <= _SCREEN_ID_LAST && target != current) {
            printf("[ui_tick] Screen change: %d (%s) -> %d (%s)\n",
                current, ui_screen_name(current),
                target,  ui_screen_name(target));
            eez_flow_set_screen((int16_t)target, LV_SCR_LOAD_ANIM_NONE, 0, 0);
        }
    }

    // Update speed from ROS
    float speed_kph;
    if (ros2subscriber_spin_some() == 0 && ros2subscriber_get_latest_speed(&speed_kph)) {
        ui_set_speed(speed_kph);
    }

    eez_flow_tick();
    tick_screen(g_currentScreen);

    // Queue any screen change request received this tick.
    // It will be applied at the top of the NEXT tick, before eez_flow_tick().
    int screen_id;
    if (ros2subscriber_get_screen_change_request(&screen_id)) {
        ui_set_screen(screen_id);
    }
}

